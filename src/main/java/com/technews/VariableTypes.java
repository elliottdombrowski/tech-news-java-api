package com.technews;

public class VariableTypes {
    public static void main(String[] args) {
        int a = 4;
        double b = 3.5;

        double sum = a + b;
        System.out.println(sum);

        String c = "dog";
        String sum2 = a + c;
        System.out.println(sum2);
        System.out.println(c + a);
    }
}
